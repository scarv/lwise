#if defined( DRIVER_TRIALS_BIST )
void test_skinny_bist( int trials_warm, int trials_real ) {
  uint8_t tk_enc[ 3 * 16 * 40 ];
  uint8_t tk_dec[ 3 * 16 * 40 ];

  uint8_t r[ 1 * 16 ];

  uint8_t k[ 3 * 16 ];
  uint8_t m[ 1 * 16 ];
  uint8_t c[ 1 * 16 ];

  for( int i = 0; i < 8; i++ ) {
    rand_bytes( ( uint8_t* )( k ), 3 * 16 * sizeof( uint8_t ) );
    rand_bytes( ( uint8_t* )( m ), 1 * 16 * sizeof( uint8_t ) );

    enc_schedule( tk_enc, k );
    memcpy( k, tk + 3 * 16 * ( 40 - 1 ), 3 * 16 * sizeof( uint8_t ) );
    dec_schedule( tk_dec, k );

    measure_step( skinny_enc, t, m, tk_enc );
    measure_step( skinny_dec, c, t, tk_dec );

    if(  memcmp( r, m, 1 * 16 * sizeof( uint8_t ) ) ||  // if r != m, i.e., Dec( k, Enc( k, m ) ) != m
        !memcmp( c, m, 1 * 16 * sizeof( uint8_t ) ) ||  // or c == m, i.e.,         Enc( k, m )   == m, so Enc is acting as a NOP!
        !memcmp( r, c, 1 * 16 * sizeof( uint8_t ) ) ) { // or r == c, i.e.,         Dec( k, c )   == c, so Dec is acting as a NOP!
      printf( "!! vector %2d : failed\n", i ); abort();
    }
  }
}
#endif

